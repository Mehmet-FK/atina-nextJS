export const getServerSideProps = async (context) => {
  const session = await getSession(context);
  console.log("SESSION", session);
  if (!session) {
    return {
      redirect: {
        destination: "/auth/login",
        permanent: false,
      },
    };
  }
  return {
    props: { session },
  };
};
